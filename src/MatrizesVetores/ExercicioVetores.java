package MatrizesVetores;

public class ExercicioVetores {

	public static void main(String[] args) {
		// Aplicação de vetores em exeplo de lista de livros,

		String vetorString[] = { "Mulheres correm com lobos", "O pequeno príncipe", "O diabo veste prada",
				"Ela fica com a garota" };

		System.out.println("Primeiro elemento " + vetorString[1]);// entra como segunda posição
		System.out.println("Segundo elemento " + vetorString[2]);
		System.out.println("Terceiro elemento " + vetorString[3]);
		System.out.println("Quarto elemento " + vetorString[0]);// posição zero é sempre a primeira
	}

}